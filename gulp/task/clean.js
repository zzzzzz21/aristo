'use strict';

//--------------------------------------------------------------------------------
// パッケージの読み込み
//--------------------------------------------------------------------------------
const gulp = require('gulp');
const del = require('del');

//--------------------------------------------------------------------------------
// 設定ファイルの読み込み
//--------------------------------------------------------------------------------
const path = require('../path');
const taskName = require('../taskName');

//--------------------------------------------------------------------------------
// タスクの定義
//--------------------------------------------------------------------------------
const execute = (isRelease) => {
	return del(isRelease ? path.releaseDir : path.buildDir);
};

//--------------------------------------------------------------------------------
// タスクの登録
//--------------------------------------------------------------------------------
gulp.task(taskName.clean.build, () => {
	return execute(false);
});

gulp.task(taskName.clean.release, () => {
	return execute(true);
});
